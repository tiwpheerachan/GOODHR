function p(text, opts = {}) {
  return new Paragraph({
    spacing: { after: opts.afterSmall ? 60 : 120 },
    alignment: opts.center ? AlignmentType.CENTER : AlignmentType.LEFT,
    children: [new TextRun({ text, size: opts.size || 21, font: "Arial", color: opts.color || DARK, bold: !!opts.bold, italics: !!opts.italic })]
  });
}
function bullet(text, level = 0) {
  return new Paragraph({
    numbering: { reference: "bullets", level },
    spacing: { after: 60 },
    children: [new TextRun({ text, size: 21, font: "Arial", color: DARK })]
  });
}
